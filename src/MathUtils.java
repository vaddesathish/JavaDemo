public class MathUtils {
        int add(int a) {
            return a + 10;
            //method with one parameter
        }
        int add(int a, int b) {
            return a + b;
            //method with two parameters
        }
        double add(double a, double b, double c) {
            return a + b + c;
            //method with three parameters
        }
        String add(String a,String b){
            return a+b;
        }
    }

